			<span class="BigBold">Su historia<br>
			</span>Si bien la fundaci&oacute;n de Aldosivi se concret&oacute; 
			el 29 de marzo de 1913, los inicios del club se remontan a octubre 
			de 1911 cuando los empleados de la Empresa Sociedad Nacional de Trabajos 
			P&uacute;blicos de Par&iacute;s constru&iacute;an el puerto de Mar 
			del Plata. 
			<p>Lo que pretend&iacute;an esos trabajadores era tener un lugar de 
			  descanso y esparcimiento, practicando alg&uacute;n deporte, como 
			  por ejemplo el f&uacute;tbol. </p>
			<p>As&iacute; fue que el 29 de marzo de 1913 en las instalaciones 
			  de la confiter&iacute;a &#8220;El Recreo&#8221;, ubicada en la hoy 
			  calle 12 de octubre y Mart&iacute;nez de Hoz, deciden fundar un 
			  club. Los primeros colores elegidos fueron los de la bandera francesa, 
			  (azul, rojo y blanco), colores que con el tiempo cambiar&iacute;an 
			  por el verde y amarillo.</p>
			<p>Uno de los inconvenientes al que arribaron fue el nombre de la 
			  instituci&oacute;n. Para ello tomaron la direcci&oacute;n telegr&aacute;fica 
			  del puerto local, que eran las dos primeras letras de los apellidos 
			  de los ingenieros y propietarios de la empresa constructora: Allard, 
			  Doulfous, Sillard y Wiriot. Como se podr&aacute; apreciar el nombre 
			  original era Al-Do-Si-Wi, pero como la letra W no se utilizaba en 
			  las direcciones telegr&aacute;ficas se lo cambi&oacute; por la &#8220;V&#8221; 
			  para quedar as&iacute; como &#8220;Aldosivi&#8221;.</p>
			<p>El primer presidente de la instituci&oacute;n fue Don Pedro Ser&eacute;, 
			  de profesi&oacute;n farmac&eacute;utico, y hombre entusiasmado con 
			  el desarrollo de los deportes.</p>
			<p>La primera sede estaba ubicada en la calle Figueroa Alcorta entre 
			  12 de Octubre y Magallanes, sede que no s&oacute;lo serv&iacute;a 
			  para reunirse con fines deportivos, sino tambi&eacute;n supo ser 
			  &aacute;mbito de diversas reuniones art&iacute;sticas, manifestaciones 
			  culturales y pol&iacute;ticas.</p>
			<p>Con el correr de los a&ntilde;os Aldosivi fue incorporando actividades 
			  deportivas, donde pasaron el b&aacute;squetbol, el ajedrez, las 
			  artes marciales, el pat&iacute;n, la nataci&oacute;n y la pr&aacute;ctica 
			  permanente del juego de bochas, donde la actual sede sirve de escenario 
			  para torneos locales, regionales y provinciales.</p>
			<p>En 1959 Aldosivi consigue ascender a la primera divisi&oacute;n 
			  &#8220;A&#8221; de Mar del Plata, tras derrotar a su cl&aacute;sico 
			  rival, Talleres F&uacute;tbol Club, por 4 a 3 en un partido que 
			  se recuerda por lo variante del resultado y por el r&eacute;cord 
			  de recaudaci&oacute;n establecido en el estadio Gral. San Mart&iacute;n 
			  de Mar del Plata.</p>
			<p>En 1973 Aldosivi consigue el primer t&iacute;tulo oficial de la 
			  Liga Marplatense de F&uacute;tbol, logro que repiti&oacute; en los 
			  dos a&ntilde;os siguientes y le dieron el pasaporte para disputar 
			  los viejos torneos nacionales de la Argentina en 1974, 1975 y 1976.</p>
			<p>Uno de los principales gestores de esta movida fue el desaparecido 
			  dirigente Adolfo Luis L&oacute;pez y por ello el estadio de f&uacute;tbol 
			  llev&oacute; su nombre.</p>
			<p>El 11 de noviembre de 1975 Aldosivi consigue el triunfo futbol&iacute;stico 
			  de mayor repercusi&oacute;n de la ciudad. Le gan&oacute; a Boca 
			  por dos a uno y nada menos que en &#8220;La Bombonera&#8221;, triunfo 
			  que ning&uacute;n otro equipo de la ciudad conquist&oacute;.</p>
			<p>La d&eacute;cada del &#8217;80 vio descender a Aldosivi a la primera 
			  &#8220;B&#8221; local. El club estaba sumergido en una profunda 
			  crisis donde los juicios y los embargos eran moneda corriente.</p>
			<p>Cuando llega 1989, ya con la presidencia de Oscar Salerno, se conforma 
			  un gran equipo de f&uacute;tbol que obtuvo el campeonato local y 
			  le dio la posibilidad de jugar por primera vez un torneo regional. 
			</p>
			<p>En 1990 Aldosivi vende su sede ubicada en Bermejo y El Cano para 
			  trasladarse a los terrenos aleda&ntilde;os a la usina &#8220;9 de 
			  Julio&#8221; en Mart&iacute;nez de Hoz y V&eacute;rtiz. All&iacute; 
			  se construy&oacute; el estadio que sirvi&oacute; para disputar torneos 
			  locales y de la Primera &#8220;B&#8221; Nacional.</p>
			<p>En 1996 con la reestructuraci&oacute;n de los torneos nacionales, 
			  Aldosivi le gan&oacute; a su rival de la d&eacute;cada del &#8217;90, 
			  Alvarado, las dos finales que hab&iacute;a dispuesto la AFA para 
			  conformar un nuevo torneo de segunda divisi&oacute;n.</p>
			<p>El punto m&aacute;s alto de las participaciones en los nuevos torneos 
			  nacionales (segunda divisi&oacute;n de la Argentina), se consigui&oacute; 
			  en 1998 cuando Aldosivi accedi&oacute; a la final contra Belgrano 
			  de C&oacute;rdoba. En Mar del Plata, fue empate en un gol y en la 
			  revancha en C&oacute;rdoba perdi&oacute; por tres a uno.</p>
			<p>En el a&ntilde;o 2000 el equipo de Mar del Plata descendi&oacute; 
			  al Torneo Argentino &#8220;A&#8221; de f&uacute;tbol, categor&iacute;a 
			  donde actualmente milita buscando un nuevo lugar cerca de la Primera 
			  divisi&oacute;n.</p>
